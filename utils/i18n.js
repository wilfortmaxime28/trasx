function normalizeLocale(locale) {
  const candidate = String(locale || 'en').toLowerCase().split('-')[0];
  return SUPPORTED_LOCALES.includes(candidate) ? candidate : 'en';
}

function createTranslator(locale) {
  const normalizedLocale = normalizeLocale(locale);

  const lookup = (dict, path) => {
    return path.split('.').reduce((acc, key) => (acc && acc[key] !== undefined ? acc[key] : undefined), dict);
  };

  return (key, fallback = '') => {
    const localized = lookup(TRANSLATIONS[normalizedLocale] || {}, key);
    if (localized !== undefined) return localized;
    const english = lookup(TRANSLATIONS.en, key);
    if (english !== undefined) return english;
    return fallback || key;
  };
}

function createSourceTextTranslator(locale) {
  const normalizedLocale = normalizeLocale(locale);

  return (sourceText, fallback = '') => {
    const normalizedSource = String(sourceText || '').trim();
    if (!normalizedSource) return fallback || normalizedSource;
    const localized = SOURCE_TEXT_TRANSLATIONS[normalizedLocale]?.[normalizedSource];
    if (localized !== undefined) return localized;
    if (normalizedLocale !== 'fr') {
      const english = SOURCE_TEXT_TRANSLATIONS.en?.[normalizedSource];
      if (english !== undefined) return english;
    }
    return fallback || normalizedSource;
  };
}

function flattenTranslations(locale) {
  const normalizedLocale = normalizeLocale(locale);
  const source = TRANSLATIONS[normalizedLocale] || TRANSLATIONS.en || {};
  const flat = {};

  const walk = (prefix, value) => {
    if (typeof value === 'string') {
      flat[prefix] = value;
      return;
    }
    if (!value || typeof value !== 'object') return;
    for (const [key, child] of Object.entries(value)) {
      const nextPrefix = prefix ? `${prefix}.${key}` : key;
      walk(nextPrefix, child);
    }
  };

  walk('', source);
  return flat;
}

function flattenSourceTextTranslations(locale) {
  const normalizedLocale = normalizeLocale(locale);
  if (normalizedLocale === 'fr') {
    return SOURCE_TEXT_TRANSLATIONS.fr || {};
  }
  return {
    ...(SOURCE_TEXT_TRANSLATIONS.en || {}),
    ...(SOURCE_TEXT_TRANSLATIONS[normalizedLocale] || {})
  };
}

